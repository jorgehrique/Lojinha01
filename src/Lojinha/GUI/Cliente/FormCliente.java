    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        if (jTable2.getSelectedRow() != -1) {
            int linha = jTable2.getSelectedRow();
            int id = (Integer) jTable2.getValueAt(linha, 0);

            clienteRN = new ClienteRN();
            Cliente cliente = clienteRN.buscarIdAtivo(id);

            int showConfirmDialog = JOptionPane.showConfirmDialog(null,
                    "Deseja excluir " + cliente.getNome() + " da sua lista de clientes ?",
                    "Confirmação de exclusão", 1);

            if (showConfirmDialog == 0) {
                clienteRN = new ClienteRN();
                clienteRN.excluir(cliente);

                //atualizar tabela
                clienteRN = new ClienteRN();
                atualizarTabela(clienteRN.listar());
            }
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um contato para exclusão.",
                    "Erro ao excluir contato", 2);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // pesquisar cliente
        if ("".equals(jTextBusca.getText())) {
            JOptionPane.showMessageDialog(null, "Escreva algo na busca", "Erro na pesquisa", 1);
        } else {
            List<Cliente> clientes = null;
            clienteRN = new ClienteRN();
            if (jComboBox1.getSelectedIndex() == 0) {
                clientes = clienteRN.buscarNome(jTextBusca.getText());

            } else if (jComboBox1.getSelectedIndex() == 1) {
                clientes = clienteRN.buscarDescricao(jTextBusca.getText());

            } else if (jComboBox1.getSelectedIndex() == 2) {
                clientes = clienteRN.buscarCidade(jTextBusca.getText());

            } else if (jComboBox1.getSelectedIndex() == 3) {
                clientes = clienteRN.buscarRua(jTextBusca.getText());
            }
            if (clientes.isEmpty()) {
                JOptionPane.showMessageDialog(null,
                        "Sua busca não trouxe resultados, tente outra palavra-chave",
                        "Cliente não encontrado", 1);
            }
            atualizarTabela(clientes);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

