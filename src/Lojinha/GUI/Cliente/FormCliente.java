    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:

        if (jTable2.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(null, "Selecione um contato para edição",
                    "Erro ao editar contato", 2);
        } else {
            int linha = jTable2.getSelectedRow();
            int id = (Integer) jTable2.getValueAt(linha, 0);

            clienteRN = new ClienteRN();
            Cliente cliente = clienteRN.buscarIdAtivo(id);

            new FormEdit(cliente).setVisible(true);
            this.dispose();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

