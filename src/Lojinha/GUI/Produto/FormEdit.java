    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // editar cliente
        if (!"".equals(jTextNome.getText())
                && !"".equals(jTextPreco.getText())) {
            try {
                float preco = Float.parseFloat(jTextPreco.getText().replaceAll(",", "."));

                produto.setNome(jTextNome.getText());
                produto.setPrecoVenda(preco);

                ProdutoRN produtoRN = new ProdutoRN();
                produtoRN.atualizar(produto);

                new FormProduto().setVisible(true);
                this.dispose();
            } catch (Throwable e) {
                JOptionPane.showMessageDialog(null,
                        "O pre√ßo deve ser um numero. Ex 1,99", "Erro ao editar produto", 2);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Preencha todos os campos.", "Erro ao editar o produto", 2);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

