    public void labels() {
        double ganho = 0;
        caixaRN = new CaixaRN();
        List<Caixa> ganhos = caixaRN.listarGanhos();
        for (int i = 0; i < ganhos.size(); i++) {
            try {
                ganho = ganho + ganhos.get(i).getPreco();
            } catch (Throwable e) {
                break;
            }
        }

        double gasto = 0;
        caixaRN = new CaixaRN();
        List<Caixa> gastos = caixaRN.listarGastos();
        for (int i = 0; i < gastos.size(); i++) {
            try {
                gasto = gasto + gastos.get(i).getPreco();
            } catch (Throwable e) {
                break;
            }
        }

        double saldo = ganho - gasto;

        jLabelSaldo.setText("R$ " + saldo);
        jLabelGastos.setText("R$ " + gasto);
        jLabelGanhos.setText("R$ " + ganho);
    }

