    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        //excluir gasto
        if (jTable1.getSelectedRow() != -1) {

            int linha = jTable1.getSelectedRow();
            int id = (int) jTable1.getValueAt(linha, 0);

            caixaRN = new CaixaRN();
            Caixa caixa = new Caixa();
            caixa = caixaRN.buscarId(id);

            int showConfirmDialog = JOptionPane.showConfirmDialog(null,
                    "Deseja excluir o registro nº " + caixa.getId() + " do caixa ?",
                    "Confirmação de exclusão", 1);

            if (showConfirmDialog == 0) {
                caixaRN = new CaixaRN();
                caixaRN.excluir(caixa);

                atualizarTabela1();
                labels();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um registro para exclusão.",
                    "Erro ao excluir o registro", 2);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

