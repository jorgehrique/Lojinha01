    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        verificarCliente();
        verificarProduto();

        double preco;
        double quantidade;

        try {
            preco = Double.parseDouble(jTextPreco.getText().replaceAll(",", "."));
            quantidade = Double.parseDouble(jTextQtd.getText().replaceAll(",", "."));

            if ((("".equals(jTextIdCliente.getText())
                    || "".equals(jTextIdProduto.getText()))
                    || "".equals(jTextQtd.getText()))
                    || "".equals(jTextPreco.getText())) {
                JOptionPane.showMessageDialog(null, "Preencha os campos vazios.", "Erro ao editar venda", 2);

            } else if (produto == null) {
                JOptionPane.showMessageDialog(null, "Digite o id de um produto válido.", "Erro ao editar venda", 2);

            } else if (cliente == null) {
                JOptionPane.showMessageDialog(null, "Digite o id de um cliente válido.", "Erro ao editar venda", 2);

            } else if (produto.getQuantidade() < Integer.parseInt(jTextQtd.getText())) {
                JOptionPane.showMessageDialog(null, "Não se pode vender mais do que tem.", "Erro ao editar venda", 2);

            } else if (produto.getQuantidade() < Integer.parseInt(jTextQtd.getText())) {
                JOptionPane.showMessageDialog(null, "Não se pode vender quantidade negativa.", "Erro ao editar venda", 2);

            } else {
                //retornando produtos
                produto.setQuantidade(produto.getQuantidade() + venda.getQuantidade());
                produtoRN = new ProdutoRN();
                produtoRN.atualizar(produto);

                // editando venda
                if (jComboBox1.getSelectedItem() == "Total") {
                    preco = preco / quantidade;
                }

                if (jRadioButton1.isSelected()) {
                    venda.setPago(true);
                } else {
                    venda.setPago(false);
                }

                venda.setCliente(cliente);
                venda.setProduto(produto);
                venda.setPreco_vendido(preco);
                venda.setQuantidade((int) quantidade);

                vendasRN = new VendasRN();
                vendasRN.atualizar(venda);

                produto.setQuantidade(produto.getQuantidade() - venda.getQuantidade());
                produtoRN = new ProdutoRN();
                produtoRN.atualizar(produto);

                // Caixa
                if (venda.isPago()) {
                    Caixa caixa = new Caixa();
                    caixa.setDescricao("Venda - " + venda.getProduto().getNome() + " (" + venda.getQuantidade() + ")");
                    caixa.setPreco(preco * quantidade);
                    caixa.setStatus(true);
                    caixa.setTipo(true);

                    SimpleDateFormat dataF = new SimpleDateFormat("dd/MM/yyyy");
                    String data = dataF.format(new Date(System.currentTimeMillis()));
                    caixa.setData_caixa(data);

                    CaixaRN caixaRN = new CaixaRN();
                    caixaRN.salvar(caixa);
                }

                new FormVendas().setVisible(true);
                this.dispose();
            }
        } catch (Throwable e) {
            JOptionPane.showMessageDialog(null,
                    "O preço e a quantidade devem ser numeros\n"
                    + "Ex: 1,99 e 10", "Erro ao salvar venda", 2);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

