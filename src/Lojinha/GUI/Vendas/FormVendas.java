    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // busca
        //int id = 0;
        String busca;

        if (!"".equals(jTextBusca.getText())) {
            List<Vendas> vendas = null;
            busca = jTextBusca.getText();

            Produto produto;
            Cliente cliente;

            if (jComboBox1.getSelectedItem() == "Cliente") {
                
                clienteRN = new ClienteRN();                
                cliente = clienteRN.buscarNome(busca).get(0);
                 
                try{
                    vendasRN = new VendasRN();
                    vendas = vendasRN.listarPorCliente(cliente);
                    
                } catch(Throwable e){
                    vendas = null;
                }   
                
            } else if (jComboBox1.getSelectedItem() == "Produto") {
               
                produtoRN = new ProdutoRN();
                produto = produtoRN.buscarNome(busca).get(0);
                
                try{
                    vendasRN = new VendasRN();
                    vendas = vendasRN.listarPorProduto(produto);
                    
                } catch(Throwable e){
                    vendas = null;
                }  

            } else if (jComboBox1.getSelectedItem() == "Data") {
                vendasRN = new VendasRN();
                
                try{
                    vendas = vendasRN.listarPorData(jTextBusca.getText());
                    
                } catch(Throwable e){
                    vendas = null;
                }
            }

            if (vendas == null) {
                JOptionPane.showMessageDialog(null,
                        "Sua busca não trouxe resultados, tente outra palavra-chave",
                        "Venda não encontrada", 1);
            }

            atualizarTabela(vendas);

        } else {
            JOptionPane.showMessageDialog(null, "Escreva algo na busca", "Erro na pesquisa", 1);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

