    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // excluir venda
        if (jTable1.getSelectedRow() != -1) {

            int linha = jTable1.getSelectedRow();
            int id = (int) jTable1.getValueAt(linha, 0);

            vendasRN = new VendasRN();
            Vendas venda;
            venda = vendasRN.buscarId(id);

            int showConfirmDialog = JOptionPane.showConfirmDialog(null,
                    "Deseja cancelar a venda nº " + venda.getId() + " ?",
                    "Confirmação de exclusão", 1);

            if (showConfirmDialog == 0) {
                vendasRN = new VendasRN();
                vendasRN.excluir(venda);

                vendasRN = new VendasRN();
                atualizarTabela(vendasRN.listar());

                produtoRN = new ProdutoRN();
                Produto produto = produtoRN.buscarId(venda.getProduto().getId());
                showConfirmDialog = JOptionPane.showConfirmDialog(null,
                        "Deseja que " + venda.getQuantidade() + " de " + produto.getNome()
                        + " volte ao estoque ?", "Confirmação de devolução", 1);
                if (showConfirmDialog == 0) {
                    produto.setQuantidade(produto.getQuantidade() + venda.getQuantidade());
                    produtoRN = new ProdutoRN();
                    produtoRN.atualizar(produto);
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Selecione uma venda para exclusão.",
                    "Erro ao cancelar a venda", 2);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

