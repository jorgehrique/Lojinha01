    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // pagar venda
        if (jTable1.getSelectedRow() != -1) {

            int linha = jTable1.getSelectedRow();
            int id = (int) jTable1.getValueAt(linha, 0);

            vendasRN = new VendasRN();
            Vendas venda;
            venda = vendasRN.buscarId(id);

            if (venda.isPago()) {
                JOptionPane.showMessageDialog(null,
                        "Essa venda já esta paga !",
                        "Erro ao quitar venda", 1);

            } else {
                int showConfirmDialog = JOptionPane.showConfirmDialog(null,
                        "Deseja dar a venda nº " + venda.getId() + " como paga ?",
                        "Confirmação de quitação", 1);

                if (showConfirmDialog == 0) {
                    venda.setPago(true);

                    vendasRN = new VendasRN();
                    vendasRN.atualizar(venda);

                    vendasRN = new VendasRN();
                    atualizarTabela(vendasRN.listar());

                    // Caixa
                    Caixa caixa = new Caixa();
                    caixa.setDescricao("Venda - " + venda.getProduto().getNome() + " (" + venda.getQuantidade() + ")");
                    caixa.setPreco(venda.getPrecoVendido() * venda.getQuantidade());
                    caixa.setStatus(true);
                    caixa.setTipo(true);

                    SimpleDateFormat dataF = new SimpleDateFormat("dd/MM/yyyy");
                    String data = dataF.format(new Date(System.currentTimeMillis()));
                    caixa.setDataCaixa(data);

                    CaixaRN caixaRN = new CaixaRN();
                    caixaRN.salvar(caixa);
                }
            }

        } else {
            JOptionPane.showMessageDialog(null, "Selecione uma venda para quitar.",
                    "Erro ao quitar venda", 2);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

